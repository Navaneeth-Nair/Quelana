use std::fs::File;
use std::io::Write;
use std::path::Path;

fn main() {
    #[cfg(target_os = "windows")]
    {
        create_minimal_icon();
        let rc_path = "resources.rc";
        if !Path::new(rc_path).exists() {
            if let Ok(mut file) = File::create(rc_path) {
                let _ = file.write_all(b"Quelana ICON \"icon.ico\"\r\n");
            }
        }
        embed_resource::compile(rc_path, embed_resource::NONE);
    }
}

